"""Build silver BIO datasets for training expansion.

Silver labels are generated by rules and lexicons, not by human annotators.
They are useful for training, but must not be used as human-gold evaluation
evidence.
"""

from __future__ import annotations

import argparse
import json
from datetime import datetime, timezone
from pathlib import Path

from annotate_bio import build_annotations, count_entities, load_yaml, split_records, write_conll


DEFAULT_CONFIG = Path("config.yaml")
DEFAULT_LEXICON = Path("resources/medical_lexicon.yaml")


def write_manifest(splits: dict, output_dir: Path, lexicon_path: Path) -> None:
    """Write machine-readable silver dataset provenance."""
    manifest = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "dataset_type": "silver",
        "annotation_source": "rules_and_medical_lexicon",
        "human_annotated": False,
        "human_gold": False,
        "lexicon": str(lexicon_path),
        "files": {
            "train": str(output_dir / "train.conll"),
            "validation": str(output_dir / "val.conll"),
            "test": str(output_dir / "test.conll"),
        },
        "split_counts": {name: len(records) for name, records in splits.items()},
    }
    (output_dir / "manifest.json").write_text(json.dumps(manifest, indent=2), encoding="utf-8")


def write_report(splits: dict, output_dir: Path) -> None:
    """Write a human-readable silver dataset report."""
    lines = [
        "# Silver Annotation Dataset",
        "",
        f"Generated at: {datetime.now(timezone.utc).isoformat()}",
        "",
        "## Status",
        "",
        "This dataset is silver-labeled by rules and lexicons. It is suitable for",
        "training experiments, but it is not a human gold test set.",
        "",
        "## Split Counts",
        "",
        "| Split | Records | Tokens | GEJALA | OBAT | DOSIS | DIAGNOSIS | ANATOMI |",
        "| --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: |",
    ]
    for split_name, records in splits.items():
        entity_counts = count_entities(records)
        token_count = sum(len(tokens) for tokens, _ in records)
        lines.append(
            "| {split} | {records} | {tokens} | {gejala} | {obat} | {dosis} | {diagnosis} | {anatomi} |".format(
                split=split_name,
                records=len(records),
                tokens=token_count,
                gejala=entity_counts["GEJALA"],
                obat=entity_counts["OBAT"],
                dosis=entity_counts["DOSIS"],
                diagnosis=entity_counts["DIAGNOSIS"],
                anatomi=entity_counts["ANATOMI"],
            )
        )
    (output_dir / "README.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    """Build silver train/validation/test CoNLL files."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--config", type=Path, default=DEFAULT_CONFIG)
    parser.add_argument("--lexicon", type=Path, default=DEFAULT_LEXICON)
    parser.add_argument("--output-dir", type=Path, default=Path("data/silver"))
    args = parser.parse_args()

    config = load_yaml(args.config)
    data_config = config["data"]
    seed = int(config["training"]["seed"])

    annotated = build_annotations(Path(data_config["clean_file"]), args.lexicon)
    splits = split_records(annotated, seed)

    args.output_dir.mkdir(parents=True, exist_ok=True)
    write_conll(splits["train"], args.output_dir / "train.conll")
    write_conll(splits["val"], args.output_dir / "val.conll")
    write_conll(splits["test"], args.output_dir / "test.conll")
    write_manifest(splits, args.output_dir, args.lexicon)
    write_report(splits, args.output_dir)

    print(
        "Wrote silver BIO splits: "
        f"train={len(splits['train'])}, val={len(splits['val'])}, test={len(splits['test'])}"
    )


if __name__ == "__main__":
    main()
